class Pet {

    id
    microchip
    species
    sex
    size
    potentDangerous
    neighborhood

    constructor(id,microchip,species,sex,size,potentDangerous,neighborhood){
        this.id=id;
        this.microchip=microchip;
        this.species=species;
        this.sex=sex;
        this.size=size;
        this.potentDangerous=potentDangerous;
        this.neighborhood=neighborhood;
    }

        get id (){
            return id;
        }

        set id(id){
            this.id=id;
        }

        get microchip (){
            return id;
        }

        set microchip(microchip){
            this.microchip=microchip;
        }

        get species (){
            return species;
        }

        set species(species){
            species=species;
        }

        get sex (){
            return sex;
        }

        set sex(sex){
            this.sex=sex;
        }

        get size (){
            return size;
        }

        set size(size){
            this.size=size;
        }

        get potentDangerous (){
            return potentDangerous;
        }

        set potentDangerous(potentDangerous){
            this.potentDangerous=potentDangerous;
        }

        get size (){
            return size;
        }

        set neighborhood(neighborhood){
            this.neighborhood=neighborhood;
        }

}